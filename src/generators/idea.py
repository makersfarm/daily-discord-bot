def build_public_data_prompt(data: dict) -> str:
    datasets = "\n".join(
        f"{i+1}. {d['title']} ({d['org']})\n"
        f"   설명: {d['desc'][:200]}\n"
        f"   키워드: {d['keywords'][:100]}\n"
        f"   원본링크: {d['url']}"
        for i, d in enumerate(data["datasets"])
    )
    return f"""오늘의 공공데이터 아이디어를 만들어줘.

분야: {data['category']}
오늘 선택된 공공데이터 API (2개):
{datasets}

각 데이터셋마다 그 데이터를 활용한 아이디어를 2개씩, 총 4개를 아래 형식으로 출력해.

아이디어 기준 (반드시 지킬 것):
- 1인 개발자가 1~2주 안에 MVP를 만들 수 있을 만큼 매우 간단·가벼운 도구/웹앱/봇.
- 복잡한 인프라, 다수 파트너십/계약, 사람이 직접 운영해야 하는 큐레이션·검수가 많이 필요한 아이디어는 지양.
- 단순 조회/필터/검색/시각화, 단발성 알림·리포트, LLM 한두 번 호출로 끝나는 가공·요약 형태 선호.
- 타겟이 명확하고 사용 동기가 분명해서 초기 사용자 유입이 쉬운 아이디어 우선.

기존 서비스 검증 (반드시 할 것):
- 아이디어마다 WebSearch 도구로 한국 시장에 동일/유사한 서비스가 있는지 검색해서 확인.
- 있으면 "✅ 유사 서비스 있음" 으로 표시하고 📎 참고 줄에 실제 서비스명+URL을 적어줘.
- 없거나 매우 약하게만 있으면 "🆕 블루오션" 으로 표시하고 📎 줄은 생략.
- 추측으로 ✅/🆕 결정하지 말고 반드시 검색 결과 기반으로 판단.

출력 규칙:
- 데이터 요약은 1줄(50자 이내), 아이디어 설명도 1줄, 기능은 핵심만 2개.
- 데이터셋 헤더 아래에 위에서 받은 원본링크를 그대로 "🔗 원본 데이터" 줄로 넣어줘.
- 추가 질문이나 부연 설명(웹 검색 권한, Sources 등) 없이 바로 형식대로만 출력.
- 아이디어 사이 구분선(---)을 꼭 넣어줘. 같은 데이터셋의 두 아이디어 사이에도 구분선을 넣어줘.
- 아래 형식의 빈 줄 위치를 그대로 지켜서 출력해.

형식 (각 데이터셋마다 한 번씩, 데이터셋 헤더 → 아이디어 2개):

## 📁 데이터셋명 (기관)
데이터 한 줄 요약
🔗 [원본 데이터](원본링크)

### 💡 아이디어명
누구를 위해 어떤 문제를 푸는지 한 줄 설명

1️⃣ 핵심 기능
2️⃣ 핵심 기능

🆕 블루오션
또는
✅ 유사 서비스 있음
📎 참고: [유사서비스명](URL)

---

### 💡 두 번째 아이디어명
... (위와 동일 형식)

---
"""


